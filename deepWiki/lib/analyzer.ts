export async function analyzeRepo(
  repoUrl: string,
  repoName: string,
  fileTree: string,
  fileContents: string,
  onChunk: (chunk: string) => void
): Promise<string> {
  const llm = createLLM()
  const repoContext = buildRepoContext(repoUrl, repoName, fileTree, fileContents)
  const batches = [
    BATCH1_PROMPT(repoName),
    BATCH2_PROMPT,
    BATCH3_PROMPT,
    BATCH4_PROMPT,
    BATCH5_PROMPT,
  ]

  let fullContent = ''
  let previousHeadings = ''

  for (let i = 0; i < batches.length; i++) {
    const contextPart = i === 0
      ? `${repoContext}\n\n---\n\n${batches[i]}`
      : `${repoContext}\n\n---\n\n## 已生成章节（保持连贯）：\n${previousHeadings}\n\n---\n\n${batches[i]}`

    const stream = await llm.stream([
      new SystemMessage(SYSTEM_PROMPT),
      new HumanMessage(contextPart),
    ])

    let batchContent = ''
    for await (const chunk of stream) {
      const text = typeof chunk.content === 'string' ? chunk.content : ''
      if (text) {
        batchContent += text
        onChunk(text)
      }
    }

    fullContent += batchContent + '\n\n'
    previousHeadings += batchContent
      .split('\n')
      .filter(line => line.startsWith('#'))
      .join('\n') + '\n'
  }

  return fullContent
}
