export function createLLM() {
  return new ChatOpenAI({
    apiKey: process.env.OPENAI_API_KEY,
    configuration: {
      baseURL: process.env.OPENAI_BASE_URL,
    },
    modelName: process.env.MODEL_NAME || 'qwen-plus',
    temperature: 0.3,
    maxTokens: 8192,
  })
}

